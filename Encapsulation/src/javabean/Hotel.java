package javabean;

public class Hotel {

	private String name;
	private double rating;
	
	public void setName(String name) {
		this.name=name;
	}
	public String getName() {
		return name;
	}
	public void setRating(double rating) {
		this.rating=rating;
	}
	public double getRating() {
		return rating;
	}
	
}
